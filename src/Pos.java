
public class Pos {
	//creating private variables
	private int Xpos;
	private int Ypos;
	
	//default constructor
	public Pos() {
		
	}
	//constructor
	public Pos(int i, int j) {
		this.Xpos = i;
		this.Ypos = j;
	}
	//getter for X
	public int getXpos() {
		return Xpos;
	}
	//getter for Y
	public int getYpos() {
		return Ypos;
	}
}
